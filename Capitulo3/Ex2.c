//Operadores Aritiméticos
#include <stdio.h>
int main(){
    int a = 25;
    int b = 5;
    printf("O resultado de %d + %d é: %d \n", a, b, a+b);
    printf("O resultado de %d - %d é: %d \n", a, b, a-b);
    printf("O resultado de %d * %d é: %d \n", a, b, a*b);
    printf("O resultado de %d / %d é: %d \n", a, b, a/b);
    printf("O resultado de %d mod %d é: %d \n", a, b, a%b);
    return 0;
}